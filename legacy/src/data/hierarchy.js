export class HierarchyService {
  static divisionsFor(command) {
    if (!command) return [];
    return DIVISIONS[command] || [];
  }

  static brigadesFor(division) {
    if (!division) return [];
    return BRIGADES[division] || ['ללא'];
  }

  static battalionsFor(brigade) {
    if (!brigade) return [];
    return BATTALIONS[brigade] || ['ללא'];
  }

  static statusColor(status) {
    switch (status) {
      case 'תקין - בשימוש': return 'status-active';
      case 'בשיפוץ': return 'status-reno';
      case 'לא בשימוש': return 'status-inactive';
      case 'בתכנון': return 'status-plan';
      case 'בבנייה': return 'status-build';
      default: return 'status-none';
    }
  }
}
