function buildInventory(maxFill = 1.0, missingChance = 0.15) {
  const inv = {};
  for (const item of INVENTORY_ITEMS) {
    if (Math.random() < missingChance) { inv[item.id] = 0; continue; }
    const baseStd = DEFAULT_STANDARDS.t2[item.id] || 1;
    const fill = (0.5 + Math.random() * 0.7) * maxFill;
    inv[item.id] = Math.max(0, Math.round(baseStd * fill));
  }
  return inv;
}

